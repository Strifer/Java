package hr.fer.zemris.java.tecaj.hw07.shell;
/**
 * This enum defines two possible statuses after executing a given command.
 * <br>
 * <b>CONTINUE</b> - signifies the user can enter more commands
 * <br>
 * <b>TERMINATE</b> - signifies termination of the shell
 * @author Filip Džidić
 *
 */
public enum ShellStatus {
	CONTINUE, TERMINATE
}
